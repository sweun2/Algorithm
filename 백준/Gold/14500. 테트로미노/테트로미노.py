n,m = map(int,input().split())
arr = []
for i in range(n):
    arr.append(list(map(int,input().split())))    
    
tetro = [
    # I (2개)
    [(0,0),(0,1),(0,2),(0,3)],
    [(0,0),(1,0),(2,0),(3,0)],

    # O (1개)
    [(0,0),(0,1),(1,0),(1,1)],

    # L (8개)
    [(0,0),(1,0),(2,0),(2,1)],
    [(0,0),(0,1),(0,2),(1,0)],
    [(0,0),(0,1),(1,1),(2,1)],
    [(0,0),(1,0),(1,1),(1,2)],
    [(0,1),(1,1),(2,1),(2,0)],
    [(0,2),(1,0),(1,1),(1,2)],
    [(0,0),(1,0),(2,0),(0,1)],
    [(0,0),(0,1),(0,2),(1,2)],

    # S/Z (4개)
    [(0,0),(0,1),(1,1),(1,2)],
    [(0,1),(1,0),(1,1),(2,0)],
    [(0,1),(0,2),(1,0),(1,1)],
    [(0,0),(1,0),(1,1),(2,1)],

    # T (4개)
    [(0,0),(0,1),(0,2),(1,1)],
    [(0,0),(1,0),(2,0),(1,1)],
    [(0,1),(1,0),(1,1),(1,2)],
    [(0,1),(1,0),(1,1),(2,1)]
]



maxv = 0
for i in range(n):
    for j in range(m):
        
        for square in tetro:
            sumv = 0
            flag = True
            for dx,dy in square:
                nx = i + dx
                ny = j + dy
                if 0<= nx < n and 0 <= ny < m:
                    sumv += arr[nx][ny]
                else:
                    flag = False
                    break
                    
            if flag:
                maxv = max(sumv, maxv)

print(maxv)
                    
                    